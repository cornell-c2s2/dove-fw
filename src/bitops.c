//========================================================================
// bitops.c
//========================================================================
// Implementation of various functions for interacting with bits of values

#include "bitops.h"

//------------------------------------------------------------------------
// Reading
//------------------------------------------------------------------------

int get_bit( int src, int n ) {
    return ( src >> n ) & 0x1;
}

//------------------------------------------------------------------------
// Writing
//------------------------------------------------------------------------

int set_bit( int src, int n ) {
    return src | ( 0x1 << n );
}

int reset_bit( int src, int n ) {
    return src & ~( 0x1 << n );
}